package co.edu.uniquindio.concesionarioCarros.model;

public abstract class Usuario {
    private String nombre;
    private String id;

    public Usuario() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
